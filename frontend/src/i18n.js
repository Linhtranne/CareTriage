// Dynamic CMS Loading Bridge
export const loadCmsContent = async () => {
  try {
    const lang = i18n.language || 'vi';
    const response = await fetch(`http://localhost:8080/api/v1/public/landing-content?lang=${lang}`);
    const result = await response.json();

    if (result.success && result.data) {
      const cmsData = result.data;
      const translations = {};

      // Map "section.key" to i18n structure
      Object.keys(cmsData).forEach(key => {
        const [section, contentKey] = key.split('.');
        if (!translations[section]) translations[section] = {};
        translations[section][contentKey] = cmsData[key];
      });

      // Inject into i18n
      i18n.addResourceBundle(lang, 'translation', translations, true, true);
    }
  } catch (err) {
    console.error('Failed to load dynamic CMS content into i18n', err);
  }
};

// Initial load
loadCmsContent();

// Listen for language changes to reload CMS content
i18n.on('languageChanged', (lng) => {
  loadCmsContent();
});

export default i18n;
